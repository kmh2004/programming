#include <stdio.h>

int main()
{
	printf("\"강병익\"의 출석점수 계산\n");
	printf("---------------------\n");
	printf("%10d %5d 시간\n", "출석시간", 60);
	printf("%10d %5d 시간\n", "결석시간", 2);
	printf("%10d %5.1f %%\n", "출석률", (60 - 2) % 60 * 100.0);
	printf("%10d %5.1f %%\n", "출석률", (60 - 2) % 60 * 100.0*15*0.01);
}